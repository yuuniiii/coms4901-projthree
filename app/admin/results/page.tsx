import { createClient } from "@/lib/supabase/server";

export default async function ResultsPage() {
  const supabase = await createClient();

  // Fetch recent captions with flavor and image info
  const { data: captions, error } = await supabase
    .from("captions")
    .select(`
      id,
      content,
      created_datetime_utc,
      humor_flavors (slug),
      images (url)
    `)
    .order("created_datetime_utc", { ascending: false })
    .limit(50);

  if (error) {
    return <div>Error loading results: {error.message}</div>;
  }

  return (
    <div className="space-y-6">
      <h1 className="text-3xl font-bold">Generation Results</h1>
      <p className="text-gray-600 dark:text-gray-400">
        Review the latest captions generated by humor flavors.
      </p>

      <div className="bg-white dark:bg-gray-900 shadow rounded-lg overflow-hidden border dark:border-gray-800">
        <table className="min-w-full divide-y divide-gray-200 dark:divide-gray-800">
          <thead className="bg-gray-50 dark:bg-gray-800">
            <tr>
              <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider w-32">Image</th>
              <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider w-40">Flavor</th>
              <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Caption Content</th>
              <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider w-48">Generated At</th>
            </tr>
          </thead>
          <tbody className="bg-white dark:bg-gray-900 divide-y divide-gray-200 dark:divide-gray-800">
            {captions.map((caption) => (
              <tr key={caption.id}>
                <td className="px-6 py-4 whitespace-nowrap">
                  <div className="w-16 h-16 rounded overflow-hidden">
                    <img src={caption.images?.url || ""} alt="Caption image" className="object-cover w-full h-full" />
                  </div>
                </td>
                <td className="px-6 py-4 whitespace-nowrap">
                  <span className="px-2 py-1 bg-blue-100 dark:bg-blue-900 text-blue-800 dark:text-blue-100 rounded text-sm font-mono">
                    {caption.humor_flavors?.slug || "N/A"}
                  </span>
                </td>
                <td className="px-6 py-4 text-sm italic">
                  "{caption.content}"
                </td>
                <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-500">
                  {new Date(caption.created_datetime_utc).toLocaleString()}
                </td>
              </tr>
            ))}
            {captions.length === 0 && (
              <tr>
                <td colSpan={4} className="px-6 py-10 text-center text-gray-500">
                  No generated captions found yet.
                </td>
              </tr>
            )}
          </tbody>
        </table>
      </div>
    </div>
  );
}

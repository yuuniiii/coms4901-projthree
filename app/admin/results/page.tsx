import { createClient } from "@/lib/supabase/server";
import Link from "next/link";

export default async function ResultsPage({
  searchParams,
}: {
  searchParams: Promise<{ flavorId?: string }>;
}) {
  const { flavorId } = await searchParams;
  const supabase = await createClient();

  // Fetch all flavors for the filter
  const { data: flavors } = await supabase
    .from("humor_flavors")
    .select("id, slug");

  // Build the query
  let query = supabase
    .from("captions")
    .select(`
      id,
      content,
      created_datetime_utc,
      humor_flavors (id, slug),
      images (url)
    `)
    .order("created_datetime_utc", { ascending: false })
    .limit(50);

  if (flavorId) {
    query = query.eq("humor_flavor_id", flavorId);
  }

  const { data: captions, error } = await query;

  if (error) {
    return <div>Error loading results: {error.message}</div>;
  }

  return (
    <div className="space-y-6">
      <div className="flex flex-col md:flex-row md:items-center justify-between gap-4">
        <div>
          <h1 className="text-3xl font-bold">Generation Results</h1>
          <p className="text-gray-600 dark:text-gray-400">
            Review the latest captions generated by humor flavors.
          </p>
        </div>

        <div className="flex items-center gap-2">
          <label htmlFor="flavor-filter" className="text-sm font-medium whitespace-nowrap">Filter by Flavor:</label>
          <select
            id="flavor-filter"
            className="p-2 border rounded bg-white dark:bg-gray-800 text-sm"
            onChange={(e) => {
              const val = e.target.value;
              window.location.href = val ? `/admin/results?flavorId=${val}` : "/admin/results";
            }}
            defaultValue={flavorId || ""}
          >
            <option value="">All Flavors</option>
            {flavors?.map((f) => (
              <option key={f.id} value={f.id}>
                {f.slug}
              </option>
            ))}
          </select>
          {/* Note: In a real app we'd use router.push, but since this is a server component, we can use a small client-side script or just a form */}
          <script dangerouslySetInnerHTML={{ __html: `
            document.getElementById('flavor-filter').onchange = function() {
              const val = this.value;
              window.location.href = val ? '/admin/results?flavorId=' + val : '/admin/results';
            };
          `}} />
        </div>
      </div>

      <div className="bg-white dark:bg-gray-900 shadow rounded-lg overflow-hidden border dark:border-gray-800">
        <table className="min-w-full divide-y divide-gray-200 dark:divide-gray-800">
          <thead className="bg-gray-50 dark:bg-gray-800">
            <tr>
              <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider w-32">Image</th>
              <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider w-40">Flavor</th>
              <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Caption Content</th>
              <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider w-48">Generated At</th>
            </tr>
          </thead>
          <tbody className="bg-white dark:bg-gray-900 divide-y divide-gray-200 dark:divide-gray-800">
            {captions.map((caption) => (
              <tr key={caption.id}>
                <td className="px-6 py-4 whitespace-nowrap">
                  <div className="w-16 h-16 rounded overflow-hidden">
                    <img src={caption.images?.url || ""} alt="Caption image" className="object-cover w-full h-full" />
                  </div>
                </td>
                <td className="px-6 py-4 whitespace-nowrap">
                  <span className="px-2 py-1 bg-blue-100 dark:bg-blue-900 text-blue-800 dark:text-blue-100 rounded text-sm font-mono">
                    {caption.humor_flavors?.slug || "N/A"}
                  </span>
                </td>
                <td className="px-6 py-4 text-sm italic">
                  "{caption.content}"
                </td>
                <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-500">
                  {new Date(caption.created_datetime_utc).toLocaleString()}
                </td>
              </tr>
            ))}
            {captions.length === 0 && (
              <tr>
                <td colSpan={4} className="px-6 py-10 text-center text-gray-500">
                  No generated captions found for this criteria.
                </td>
              </tr>
            )}
          </tbody>
        </table>
      </div>
    </div>
  );
}
